console.log("This simple program will calculate the volume of a box")
let width = prompt("Volume of the box, enter width", 0); //allows user to input values
let height = prompt("Volume of the box, enter height", 0); //allows user to input values
let lenght = prompt("Volume of the box, enter lenght", 0); //allows user to input values
//and stores them in variables
let vol=width*height*lenght //calculation of the volume of the box
alert ("calculated box volume is " + vol) //displays the result of the calculation to the user